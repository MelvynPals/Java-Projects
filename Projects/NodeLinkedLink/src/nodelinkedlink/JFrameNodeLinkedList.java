    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        Node node = new Node(jTextField1.getText(), jTextField2.getText());
        int idx = Integer.parseInt(JOptionPane.showInputDialog("Enter at index\n(remember that first index is 1):  "));
        nllObj.insert(idx-1,node);
        int size = nllObj.getSize();
        
        if (idx > size){
            JOptionPane.showMessageDialog(null, "The index " + idx + " is out of bounds");
            return;
        } 
        
        JOptionPane.showMessageDialog(null, "Successfully added " + " ID: " + jTextField1.getText() + " Name: " + jTextField2.getText() + " at index " + idx);
        
        jTextField1.setText("");
        jTextField2.setText("");
    }//GEN-LAST:event_jButton3ActionPerformed

