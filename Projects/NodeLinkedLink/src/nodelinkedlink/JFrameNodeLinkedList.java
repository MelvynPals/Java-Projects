    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        int idx = Integer.parseInt(JOptionPane.showInputDialog("Enter at index\n(remember that first index is 1): "));
        nllObj.clearAt(idx-1);
        
        int size = nllObj.getSize();
        
        if (idx > size){
            JOptionPane.showMessageDialog(null, "The index " + idx + " is not found.");
            return;
        } 
        
        JOptionPane.showMessageDialog(null,"The list at index " + idx + " is now removed.");
    }//GEN-LAST:event_jButton4ActionPerformed

